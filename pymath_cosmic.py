"""
PyMath - A simple custom Python math library by Cosmic.

Provides basic math operations:
- Addition
- Subtraction
- Multiplication
- Division
"""

def getMathPlus(a, b):
    return a + b

def getMathMinus(a, b):
    return a - b

def getMathMulti(a, b):
    return a * b

def getMathDivide(a, b):
    return a / b
